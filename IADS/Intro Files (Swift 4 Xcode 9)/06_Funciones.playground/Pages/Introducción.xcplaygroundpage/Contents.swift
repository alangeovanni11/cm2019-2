 ## Vestirte
 
 Imagina que un amigo te invita a ver una película. Dormiste hasta tarde, entonces le contestas: “Nos vemos ahí. Solo tengo que vestirme”.
 
 Tu amigo sabe a qué te refieres con “vestirte”, pero no conoce los detalles, por ejemplo, qué camiseta vas a ponerte.
 
 Ahora piensa en los detalles relacionados con todo lo que hiciste para vestirte hoy: decidir qué ponerte, tomar las prendas de un ropero o una cómoda, ponerte cada prenda.
 
 La palabra “vestirse” parece referirse a algo muy simple, pero, al considerar cada detalle que entra en juego cuando te vistes, entiendes que esa palabra representa una gran complejidad.
 
 Usar una palabra como “vestirse” sirve para hablar y pensar en el proceso de ponerse ropa sin necesidad de considerar cada detalle específico.
 
 Hacemos esto todo el tiempo. Frases como “ir al dentista”, “preparar un sándwich” o “ir a tomar un café” describen actividades de las que sería muy tedioso hablar si fuéramos a mencionar cada paso en detalle.
 
 La idea de definir un modo más sencillo para hablar de algo complejo se denomina *abstracción*. La abstracción es una idea fundamental dentro del desarrollo de software. Es el método que usan los programadores para evitar que los detalles y la complejidad los abrumen.
 
 En esta lección, aprenderás a definir y usar _funciones_, una de las maneras fundamentales de definir la abstracción en la programación.
 
