## Llamar a una función
 
Tal vez no lo notaste en el momento, pero ya aprovechaste la abstracción y las funciones cuando usaste `print()` para imprimir “¡Hola, mundo!” en la consola. En Swift, `print()` es una función. Cuando la usas, estás _llamando_ a la función:
*/
