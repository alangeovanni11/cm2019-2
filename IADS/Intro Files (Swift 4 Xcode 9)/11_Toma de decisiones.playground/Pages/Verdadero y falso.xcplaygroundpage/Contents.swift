 ## Verdadero y falso
 
 En el juego de las 20 preguntas, puedes identificar cualquier objeto del universo haciendo preguntas cuya respuesta sea *sí* o *no*. Las preguntas que tienen respuestas simples pueden ser muy eficaces.
 
 Puedes escribir código pensado como una pregunta, y Swift indicará si la respuesta es _verdadera_ o _falsa_. Puedes ver los resultados en la barra lateral, como cuando escribiste los cálculos.
 
 De hecho, como puedes ver, `true` (verdadero) y `false` (falso) son valores especiales en Swift:
*/
