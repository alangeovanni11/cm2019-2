/*:
 ## Ejercicio: Práctica de las instrucciones condicionales “else”
 
 Seguramente recuerdes que las instrucciones condicionales `else` tienen código que se ejecuta cuando el resultado de una instrucción condicional `if` es “false” (falso). De esa forma, se puede reducir la cantidad de código necesario y también se puede entender más claramente cuál debería ser el flujo de un programa.
*/
let secretWord = "plátano"
let guess = "manzana"

if secretWord == guess {
    print("Adivinaste.")
}
if secretWord != guess {
    print("No adivinaste.")
}
