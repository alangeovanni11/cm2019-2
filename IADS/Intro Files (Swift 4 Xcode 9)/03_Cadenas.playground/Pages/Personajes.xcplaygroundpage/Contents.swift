//: ## Caracteres
//: Por supuesto, un texto puede incluir otros elementos además de letras, como signos de puntuación, números, símbolos e incluso espacios. Todos estos elementos se conocen como _caracteres_, y las _cadenas_ están compuestas por _caracteres_.
//:
//: Las letras son caracteres:
//: ## Aa Bb Cc Dd Ee
//: Y también lo son los signos de puntuación:
//: ## ! ? . , ; :
//: Números:
//: ## 1 2 3 4 5 6 7 8 9 0
//: Símbolos:
//: ## # @ $ & © π * + - /
//: Incluso hay caracteres especiales, como espacios y tabulaciones:\
//: _(No puedes verlos, pero están ahí)._
//: ## `                         `
//:
//:
//: Ahora que ya conoces las partes, es momento de crear algunas cadenas.\
