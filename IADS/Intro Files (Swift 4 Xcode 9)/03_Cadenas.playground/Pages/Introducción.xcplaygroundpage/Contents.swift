//: ## Cadenas
//: Si miras cualquier app, notarás la presencia de texto. El texto aparece en todos lados: apps de mensajes, lectores de noticias, apps de redes sociales y más. Incluso las apps de cámara pueden tener texto para los nombres de botones y controles.
//:
//: En la mayoría de los lenguajes de programación, incluido Swift, los valores de texto se conocen como _cadenas_.
//:
//: ### ¿Por qué se denominan cadenas?
//: Imagina una cadena de cuentas que forman un collar. Cada cuenta puede ser diferente, pero todas están enlazadas en un orden particular.
//:
//: ![cadena de cuentas](StringOfBeads.png)
//: En general, una única letra (por ejemplo, una “H”) no es útil por sí sola. Las palabras están compuestas por una cadena de letras. Por lo tanto, los programadores llaman _cadena_ al texto (o, en inglés, "string"). Una cadena puede ser cualquier texto que imagines.
//:
//: ![cadena de caracteres](StringOfCharacters.png)
//:
//: A continuación, aprenderás qué otros elementos, además de las letras, pueden formar una cadena.
