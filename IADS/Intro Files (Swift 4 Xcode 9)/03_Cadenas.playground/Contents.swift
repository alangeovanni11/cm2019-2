//: ## Cadenas
//: Si miras cualquier app, notarás la presencia de texto. El texto aparece en todos lados: apps de mensajes, lectores de noticias, apps de redes sociales. Incluso las apps de cámara pueden tener texto para los nombres de algunos botones y controles. En informática, los valores de texto casi siempre se denominan _cadenas_.
//:
//: ### ¿Por qué se denominan cadenas?
//: Imagina una cadena de cuentas que forman un collar. Cada cuenta puede tener una apariencia (o, incluso, una forma) diferente, pero están enlazadas en un orden particular.
//: ![cadena de cuentas](StringOfBeads.png)
//: En general, tener un único carácter, como la letra “H”, no es algo muy útil. Necesitamos una cadena completa de caracteres combinados para crear palabras, oraciones, párrafos, etc. Por lo tanto, los programadores llaman _cadena_ a los datos de texto.
