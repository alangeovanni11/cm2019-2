 ## Cambiar los colores

Muchos programadores quieren configurar su entorno informático con un estilo que les parezca cómodo. Para hacer cambios en tu entorno de Xcode, selecciona Xcode > Preferences… (Preferencias…) en la barra de menús:

 ![Barra de menú con el menú de Xcode abierto ](xcode-preferences.png)

En la ventana Preferences (Preferencias), selecciona la pestaña Fonts &amp; Colors (Tipos y colores), y asegúrate de que esté seleccionado el Source Editor (Editor de código):

 ![Panel de preferencias de Xcode que muestra la pestaña Fonts and Colors (Tipos y colores)](xcode-pref-fonts-and-colors.png)


Para lograr un estilo que parezca totalmente "retro", elige una de las tres opciones con fondo oscuro (Civic [Civil], Dusk [Anochecer] o Midnight [Medianoche]):

 ![Panel de preferencias de Xcode que muestra la pestaña Fonts and Colors (Tipos y colores) con la opción Dusk (Anochecer) seleccionada](xcode-pref-dusk-color.png)


¿El cambio dificultó la legibilidad del código? No te preocupes: son _tus_ preferencias, y puedes elegir la apariencia que quieras para tu código. Si, con el fondo oscuro, puedes leer el código, consérvalo en las páginas siguientes del playground, en las cuales podrás agregar más efectos de estilo antiguo.
 
