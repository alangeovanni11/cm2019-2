/*:
 ## ¿Qué puedes imprimir?
 
 En la consola, se puede imprimir con la instrucción `print` cualquier resultado de la barra lateral de resultados.
 
 Abre la barra lateral de resultados y consulta los ejemplos para confirmar esto.
 
 >(Nota):\
>Si ves `\n` en las instrucciones "print" de la barra lateral de resultados, significa que `print` agrega una nueva línea al final de la cadena. De lo contrario, la consola imprime todo en una misma línea.
 
 Puedes imprimir cadenas:
*/
"¡Hola, mundo!"
print("¡Hola, mundo!")
//: Números y cálculos:
8
print(8)
7 + 11
print(7 + 11)
//: Expresiones de cadena:
"Has visto" + "..." + "descuida."
print("Has visto" + "..." + "descuida.")
//: Constantes:
let authorName = "Beatrix Potter"
print(authorName)
let bookTitle = "Jemima Puddleduck"
print(bookTitle)
