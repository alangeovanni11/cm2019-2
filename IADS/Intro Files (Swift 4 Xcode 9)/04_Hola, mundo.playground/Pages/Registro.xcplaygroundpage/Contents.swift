 ## Registro
 
 Tal vez hayas oído hablar del registro de un capitán, en el cual el capitán de un barco (o de una nave espacial) registra toda la información diaria sobre el recorrido de la nave. Pero ¿sabías que las apps también pueden tener registros?
 
 Cuando los programadores imprimen mensajes en la consola, por lo general, lo hacen para _registrar_ información sobre un programa a medida que se ejecuta. La acción de imprimir mensajes en la consola se conoce como _registrar_ y, a veces, los mensajes se denominan _mensajes de registro_.
 
 Con frecuencia, los programadores registran mensajes para indicar algún tipo de problema o imprevisto. Como verás a continuación, los mensajes pueden dar advertencias y ayudar a diagnosticar problemas.
 
