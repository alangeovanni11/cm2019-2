 ## Instancias
 
 Ya trabajaste con una variedad de tipos en Swift, como, por ejemplo, `String`. Un valor de `String` individual, como “hello”, es una _instancia_ de ese tipo.
 
 - `String` es un tipo.
 - `“hello”` es una _instancia_ de `String`.
 
Ya hace tiempo que vienes creando instancias del tipo `Int` y `String`. De hecho, cada valor que creas en un programa es una instancia de su tipo.
 
Las constantes `hello` (hola) y `aDifferentHello` (un saludo diferente) son dos instancias de `String`, que casualmente tienen los mismos datos.

