 ## Constante y variable
 
En programación, hay una diferencia muy importante entre los nombres que siempre se refieren al mismo valor y los nombres cuyo valor puede cambiar con el tiempo.
 
 Esta diferencia también existe al nombrar objetos en la vida real, pero las personas no suelen notarlo.
 
 #### Constante
 
 Por ejemplo, piensa en tu “fecha de nacimiento”. Tu fecha de nacimiento no cambia. Independientemente de tu edad o de cuánto crezcas, esa fecha permanece igual. Incluso dentro de 100 años, cuando la gente lea acerca de cómo tus fantásticas apps cambiaron el mundo, tu fecha de nacimiento será la misma.
 
 Tu fecha de nacimiento es una constante. El valor se establece una sola vez cuando naces y no cambia nunca más. En programación, usarías `birthdate` (fecha de nacimiento) como una constante.
 
 
 #### Variable
 
 Muchos de los nombres que usamos todos los días pueden adquirir valores distintos con el paso del tiempo.
 
 Por ejemplo, piensa en tu camiseta favorita o en cualquier prenda de vestir que sea tu favorita. En tu infancia, probablemente tuviste una camiseta favorita. A medida que fuiste creciendo, el tamaño o el estilo de esa camiseta ya no se adaptaba a tus gustos, por lo que otra camiseta pasó a ser tu favorita. Más adelante, es posible que tengas otra camiseta favorita. Aún conservas la idea de una “camiseta favorita”, pero la camiseta a la que haces referencia va cambiando con el tiempo. En programación, dirías que `favoriteShirt` (camiseta favorita) es una variable.
 
 #### Un poco de ambas
 En la mayoría de los deportes de equipos, es posible ver un poco de ambas ideas de constantes y variables. Existe la noción de “equipo local” y “visitantes”. En tu institución educativa, el equipo local siempre es el mismo. Para la institución, es constante. El nombre o concepto de los “visitantes” permanece igual, pero el equipo en sí cambia en cada partido. El equipo visitante es variable.
 
