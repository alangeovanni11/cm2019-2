/*:
Tal vez empieces a ver un patrón. Una gran parte de la programación es inventar elementos, darles nombres y llamarlos por su nombre para usarlos.
 
 Hasta ahora, usaste constantes para asignar un valor a un nombre. Pero aún queda una pregunta sin responder: ¿por qué se denominan constantes?
 
 Es posible que ya hayas deducido que el nombre “constante” tiene que ver con permanecer igual (es decir, permanecer constante) con el tiempo.
 
 Una vez que asignas el valor de una constante, puedes confiar en que el valor será siempre el mismo. El valor asociado con ese nombre permanece constante.
 
 Existe una segunda manera de asociar un valor con un nombre en Swift. Esta manera se denomina _variable_.
 
 Con una variable, el valor se puede actualizar posteriormente a medida que se ejecuta el programa. El valor asociado con el nombre puede cambiar (variar) con el tiempo.
 
 En este playground, aprenderás más detalles sobre las diferencias entre las variables y las constantes, cómo declarar variables y cómo decidir si usar una variable en vez de una constante.
 
 ¿Cómo funciona todo esto y por qué es importante?
 
