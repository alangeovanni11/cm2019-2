/*:
 Los valores que no se pueden cambiar se llaman **constantes**, porque su valor es una constante. Cuando usas `let` para declarar un valor, estableces que ese valor nunca cambiará.
 
 Esto suele ser muy útil. A veces, no quieres que los valores cambien o no quieres cambiarlos accidentalmente. Pero a veces, _sí_ quieres poder cambiar los valores. En ese caso, necesitas **variables** en lugar de constantes. 
